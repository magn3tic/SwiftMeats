                <h3>Cooking Directions:</h3>
                <ol>
                    <li>Place pork cubes on large plate. Combine brown sugar, garlic salt, chipotle, chili, and onion powder. Sprinkle over pork; toss to evenly coat pork with seasoning mixture; set aside.</li>
                    <li>Bring 1 1/2 inches of water to boil in large skillet. Add bacon to boiling water; return to a boil. Boil, uncovered, for 1 minute. Drain; immediately rinse with cold water. Cut bacon lengthwise in half. Thread bacon accordion-style onto 8 to 10 inch long skewers with seasoned pork cubes, leaving a small amount of space between pieces.</li>
                    <li>Place beans in saucepan or small Dutch oven that is safe for on the grill.</li>
                    <li>Preheat gas grill to medium for direct grilling. Grill kabobs, uncovered, for 10 to 15 minutes or until pork is tender, turning occasionally. Warm beans on grill side burner or on grill grate over direct heat until heated through.</li>
                    <li>Serve beans with kabobs.</li>
