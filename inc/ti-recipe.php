                <h3>Ingredients:</h3>
                <ul class="ingredient-list">
                    <li>One pound Swift PremiumÂ® Boneless Pork Sirloin or Rib Roast, cut into 1 to 1 1/4 inch cubes
                    <li>1 tablespoon packed brown sugar
                    <li>1 1/4 teaspoons garlic salt
                    <li>1/2 teaspoon chipotle chile powder
                    <li>1/2 teaspoon chili powder
                    <li>1/4 teaspoon onion powder
                    <li>4 slices thick-sliced bacon
                    <li>1 16-ounce can baked beans in sauce
